# include<stdio.h>

int main()
{
    int a, b;
    printf("Enter the value of a:");
    scanf("%d", &a);
    printf("Enter the value of b:");
    scanf("%d", &b);

    //likewise you can also go for subtraction, multiplication, division, etc. by using the same logic but different operators.
    printf("The sum of the equation is : %d", a + b);
    return 0;
}